`timescale 1ns / 1ps

module Graphic(
    input clk,
    //input [95:0] view,
    output reg [23:0] row,
    output reg [3:0] col
    );
    reg [95:0] view;
    ClockReducer_1_2 c(clk, red_clk);
    initial begin
        view = 0;
        row = 24'b111111111111111111111110;
        col = view[95:92];
    end

    always @(posedge red_clk) begin
        view = view + 1;
        case (row)
            24'b011111111111111111111111: begin col <= view[95:92]; row <= 24'b111111111111111111111110; end
            24'b111111111111111111111110: begin col <= view[91:88]; row <= 24'b111111111111111111111101; end
            24'b111111111111111111111101: begin col <= view[87:84]; row <= 24'b111111111111111111111011; end
            24'b111111111111111111111011: begin col <= view[83:80]; row <= 24'b111111111111111111110111; end
            24'b111111111111111111110111: begin col <= view[79:76]; row <= 24'b111111111111111111101111; end
            24'b111111111111111111101111: begin col <= view[75:72]; row <= 24'b111111111111111111011111; end
            24'b111111111111111111011111: begin col <= view[71:68]; row <= 24'b111111111111111110111111; end
            24'b111111111111111110111111: begin col <= view[67:64]; row <= 24'b111111111111111101111111; end
            24'b111111111111111101111111: begin col <= view[63:60]; row <= 24'b111111111111111011111111; end
            24'b111111111111111011111111: begin col <= view[59:56]; row <= 24'b111111111111110111111111; end
            24'b111111111111110111111111: begin col <= view[55:52]; row <= 24'b111111111111101111111111; end
            24'b111111111111101111111111: begin col <= view[51:48]; row <= 24'b111111111111011111111111; end
            24'b111111111111011111111111: begin col <= view[47:44]; row <= 24'b111111111110111111111111; end
            24'b111111111110111111111111: begin col <= view[43:40]; row <= 24'b111111111101111111111111; end
            24'b111111111101111111111111: begin col <= view[39:36]; row <= 24'b111111111011111111111111; end
            24'b111111111011111111111111: begin col <= view[35:32]; row <= 24'b111111110111111111111111; end
            24'b111111110111111111111111: begin col <= view[31:28]; row <= 24'b111111101111111111111111; end
            24'b111111101111111111111111: begin col <= view[27:24]; row <= 24'b111111011111111111111111; end
            24'b111111011111111111111111: begin col <= view[23:20]; row <= 24'b111110111111111111111111; end
            24'b111110111111111111111111: begin col <= view[19:16]; row <= 24'b111101111111111111111111; end
            24'b111101111111111111111111: begin col <= view[15:12]; row <= 24'b111011111111111111111111; end
            24'b111011111111111111111111: begin col <= view[11:8]; row <= 24'b110111111111111111111111; end
            24'b110111111111111111111111: begin col <= view[7:4]; row <= 24'b101111111111111111111111; end
            24'b101111111111111111111111: begin col <= view[3:0]; row <= 24'b011111111111111111111111; end
        endcase
    end
endmodule
